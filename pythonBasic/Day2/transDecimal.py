# 진수 변환
# bin() 2진수 변환  0b ~~
# oct() 8진수 변환 0o ~~
# hex() 16진수 변환 0x ~~

print(bin(11)) # 0b1011
print(bin(0b0011)) # 0b11
print(bin(0o03)) # 0b11
print(bin(0xff)) # 0b11111111

print(oct(11)) # 0o13
print(oct(0b001011)) # 0o13
print(oct(0xb)) # 0o13

print(hex(0b10101)) # Ox15
print(hex(0o25)) # 0x15
print(hex(21)) # 0x15

