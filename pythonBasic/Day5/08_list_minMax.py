# 리스트의 값 중 최대값/최소값 반환
scores = [90, 78, 81, 64, 99]
print('최대 :', max(scores))  # max는 리스트의 최대값 반환
print('최소 :', min(scores))  # min는 리스트의 최대값 반환

chr = ['b','A','e','C']
print('최대 :', max(chr)) # e
print('최소 :', min(chr)) # A

# 문자열의 경우 첫 글자의 크기를 비교한다
x = ['apple', 'banana', 'Coconut', 'melon']
print('정렬 : ', sorted(x))
print('최대값 : ', max(x)) # melon
print('최소값 : ', min(x)) # Coconut

